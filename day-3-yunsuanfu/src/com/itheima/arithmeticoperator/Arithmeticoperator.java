package com.itheima.arithmeticoperator;

public class Arithmeticoperator {
    public static void main(String[] args) {
        // +
        System.out.println(1 + 2 );
        // -
        System.out.println(1 - 2 );
        // +
        System.out.println(1.1 + 2.2 );
        // *
        System.out.println( 12 * 24 );
    }
}
